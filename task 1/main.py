n=int(input("Введите целое число: ")) #вводим число
if n%2==0: #прописываем условие для определения четности числа
    print("Число четное ") #выводим "Число четное",если условия выполняются 
else: #выполняем этот блок,если условие не выполняется
    print("Число нечетное ") #выводим "Число нечетное",если условия не выполняются