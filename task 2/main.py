k=int(input("Введите число k: ")) #Вводим число k
n=int(input("Введите число n: ")) #ВВодим число n
if k<n: #Проверяем,меньше ли число k числа n
    print(k) #Выводим число k,если условие выполняется 
else: #выполняем этот блок ,если условие не выполняется
    print (n) #выводим число n ,если условие не выполняется
if k==n: #проверяем равно ли число k числу n
    print("Числа равны") #выводим "Числа равны" ,если условие выполняется